package fr.umlv.projet.graphic;

import javax.swing.JFrame;

@SuppressWarnings("serial")
public class Window extends JFrame {

	/**
	 * Class constructor
	 */
	public Window() {
		this.setTitle("2048");									//Define title
		this.setSize(750, 525);									//Define dimensions
		this.setLocationRelativeTo(null);						//Position to window's center
		this.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);	//Click X = close
		this.setResizable(false);								//Cannot resize
		this.setVisible(true);									//Window is visible 
	}
}